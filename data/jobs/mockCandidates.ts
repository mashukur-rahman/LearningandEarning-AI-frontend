export const getRandomCandidates = (
  count: number,
  excludeIds?: string[]
): Candidate[] => {
  const filtered = mockCandidates.filter(
    (c) => !excludeIds?.includes(c.id)
  );
  const shuffled = [...filtered].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, filtered.length));
};
